CREATE TABLE IF NOT EXISTS filmes(
    id INT NOT NULL AUTO_INCREMENT,
    titulo VARCHAR(20) NOT NULL,
    descricao VARCHAR(50) NOT NULL,
    foto VARCHAR(100) NOT NULL,
    video VARCHAR(100) NOT NULL,
    genero VARCHAR(15) NOT NULL,
    PRIMARY KEY(id)
);
CREATE TABLE IF NOT EXISTS confHome(
    id INT NOT NULL AUTO_INCREMENT,
    imageHeaderH VARCHAR(100) NOT NULL,
    imageHeaderH2 VARCHAR(100) NOT NULL,
    titulo VARCHAR(20) NOT NULL,
    PRIMARY KEY(id)
);
CREATE TABLE IF NOT EXISTS confFL(
    id INT NOT NULL AUTO_INCREMENT,
    imageHeaderF VARCHAR(100) NOT NULL,
    imageHeaderL VARCHAR(100) NOT NULL,
    logoSite VARCHAR(100) NOT NULL,
    pint VARCHAR(100) NOT NULL,
    twi VARCHAR(100) NOT NULL,
    face VARCHAR(100) NOT NULL,
    PRIMARY KEY(id)
);